name = '小金 '
msg = '哈喽 ，' + name + '早上好'
print (msg)
a = 10 ** 5 #求乘方
print (a)
b = 10 % 4 #求余数
print (b)
a = '变量' #变量不能以数字开头，不可以使用除下划线以外的符号，不可以使用预先设置了用途的字符串
s = 'aaa' + 'bbb' + 'ccc'#字符串的加减运算
print (s)
apple = 350
msg = '苹果' + str(apple) + '日元'#sir函数将数值或变量转化为字符串,或者在上面的350加上引号
print (msg)
s = 'Hello!' * 3#字符串的乘法运算
print(s)
print('1\n2\n3')#\为转义字符，\n表示换行符
print("He said, \"Hello\" ")#在字符串中加入双引号
print('He said, "Hello"')#或者直接用单引号
print('He didn\'t come')#单引号下用\'表示句子中的单引号，或者直接用双引号也可以
#常见的转义字符：\\表示\  \'表示' \"表示" \n表示换行 \t表示横向跳转到下一制表符位置
